a = 10000;
b = 10;
let c;
c = a + b; //add
c = a - b; //sub
c = a / b; //div
c = a * b; //mul
c = a ** b; //square
c = --a; //decrement
c = ++a; //increment
c = a % b; //modulus
console.log(c);

function addition(sum1, sum2) {
  return sum1 + sum2;
}
function subtraction(sum1, sum2) {
  return sum1 - sum2;
}
function mul(sum1, sum2) {
  return sum1 * sum2;
}
function div(sum1, sum2) {
  return sum1 / sum2;
}

